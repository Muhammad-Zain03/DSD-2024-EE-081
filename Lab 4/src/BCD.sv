module BCD
(
    input logic  [4:0] in,
    output logic [7:0] out
);

always_comb begin

    case(in)
    5'b00000 : out = 8'b0000_0000;
    5'b00001 : out = 8'b0000_0001;
    5'b00010 : out = 8'b0000_0010;
    5'b00011 : out = 8'b0000_0011;
    5'b00100 : out = 8'b0000_0100;
    5'b00101 : out = 8'b0000_0101;
    5'b00110 : out = 8'b0000_0110;
    5'b00111 : out = 8'b0000_0111;
    5'b01000 : out = 8'b0000_1000;
    5'b01001 : out = 8'b0000_1001;
    5'b01010 : out = 8'b0001_0000;
    5'b01011 : out = 8'b0001_0001;
    5'b01100 : out = 8'b0001_0010;
    5'b01101 : out = 8'b0001_0011;
    5'b01110 : out = 8'b0001_0100;
    5'b01111 : out = 8'b0001_0101;
    5'b10000 : out = 8'b0001_0110;
    5'b10001 : out = 8'b0001_0111;
    5'b10010 : out = 8'b0001_1000;
    5'b10011 : out = 8'b0001_1001;
    5'b10100 : out = 8'b0010_0000;
    5'b10101 : out = 8'b0010_0001;
    5'b10110 : out = 8'b0010_0010;
    5'b10111 : out = 8'b0010_0011;
    5'b11000 : out = 8'b0010_0100;
    5'b11001 : out = 8'b0010_0101;
    5'b11010 : out = 8'b0010_0110;
    5'b11011 : out = 8'b0010_0111;
    5'b11100 : out = 8'b0010_1000;
    5'b11101 : out = 8'b0010_1001;
    5'b11110 : out = 8'b0011_0000;
    5'b11111 : out = 8'b0011_0001;
    default : out  = 8'b0000_0000;

    endcase
end

endmodule